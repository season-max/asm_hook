package com.zhangyue.ireader.plugin_handleThread

class PluginParams {

    /**
     * 是否打印日志
     */
    boolean printLog

    /**
     * 是否启用插件
     */
    boolean turnOn

    /**
     * 是否启用线程池优化
     */
    boolean enableThreadPoolOptimized

    /**
     * 是否启用执行定时任务的线程池优化
     */
    boolean enableScheduleThreadPoolOptimized

}